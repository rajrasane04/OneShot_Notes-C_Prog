// Conditional Statements   :--->


// if, else, and else if :


void main() {

    int a = 0;

    if (a > 0) {
        printf("Positive\n");
    } else if (a < 0) {
        printf("Negative\n");
    } else {
        printf("Zero\n");
    }

}

//---------------------------------------------------------------------------------------------------------------------------------------

// switch Statement :

void main(){
    int a ;

    switch (a) {
        case 1:
            printf("One\n");
            break;
        case 2:
            printf("Two\n");
            break;
        default:
            printf("Other\n");
    }

}


