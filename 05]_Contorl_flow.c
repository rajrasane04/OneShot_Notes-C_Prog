// Loops  :--->


// for Loop :

void main(){

    for (int i = 0; i < 10; i++) {
        printf("%d ", i);
    }

}

//---------------------------------------------------------------------------------------------------------------------------------------

// while Loop :

void main(){

    int i = 0;
    while (i < 10) {
        printf("%d ", i);
        i++;
    }

}

//---------------------------------------------------------------------------------------------------------------------------------------

// do-while Loop :

void main(){
    int i = 0;
    do {
        printf("%d ", i);
        i++;
    } while (i < 10);

}
